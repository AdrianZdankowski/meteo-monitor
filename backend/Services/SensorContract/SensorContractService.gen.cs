    public partial class SensorContractService: SensorContractServiceBase
    {
        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.IWeb3 web3, SensorContractDeployment sensorContractDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<SensorContractDeployment>().SendRequestAndWaitForReceiptAsync(sensorContractDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.IWeb3 web3, SensorContractDeployment sensorContractDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<SensorContractDeployment>().SendRequestAsync(sensorContractDeployment);
        }

        public static async Task<SensorContractService> DeployContractAndGetServiceAsync(Nethereum.Web3.IWeb3 web3, SensorContractDeployment sensorContractDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, sensorContractDeployment, cancellationTokenSource);
            return new SensorContractService(web3, receipt.ContractAddress);
        }

        public SensorContractService(Nethereum.Web3.IWeb3 web3, string contractAddress) : base(web3, contractAddress)
        {
        }

    }


